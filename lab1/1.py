for m in range(253,255):
    res = 0
    nf = 1
    for n in range(m):
        nf *= (n if n else 1)
        res += (((-1)**n)*(x**(2*n+1)))/((nf if n else 1) *(2*n+1))
    print(f'{m}) {res*2/(pi)**(1/2)}')

